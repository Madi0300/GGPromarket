export const products = productList.map((item, index) => {
  const slug = toSlug(item.name);
  return {
    id: `sku-${index + 1}`,
    slug,
    title: item.name,
    price: {
      current: item.price,
      old: item.discount && item.discount < item.price ? item.discount : null,
      currency: "RUB",
    },
    origin: item.country,
    availability: {
      inStock: true,
      isHit: Boolean(item.isHit),
      hasDiscount:
        typeof item.discount === "number" && item.discount < item.price,
    },
    rating: {
      score: item.rate,
      reviewsCount: item.comments,
    },
    media: {
      thumbnail: null,
      gallery: [],
    },
    categories: item.categories,
  };
});

